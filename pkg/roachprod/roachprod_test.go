func nilLogger() *logger.Logger {
	lcfg := logger.Config{
		Stdout: io.Discard,
		Stderr: io.Discard,
	}
	l, err := lcfg.NewLogger("" /* path */)
	if err != nil {
		panic(err)
	}
	return l
}

func TestSnapshotName(t *testing.T) {
	tests := []struct {
		name      string
		prefix    string
		version   string
		nodeCount int
		numStores int
		node      int
		storeIdx  int
		expected  string
	}{
		{
			name:      "single store",
			prefix:    "snap",
			version:   "v24.1",
			nodeCount: 3,
			numStores: 1,
			node:      1,
			storeIdx:  1,
			expected:  "snap-v24-1-n3-0001",
		},
		{
			name:      "multi store",
			prefix:    "snap",
			version:   "v24.1",
			nodeCount: 3,
			numStores: 4,
			node:      1,
			storeIdx:  1,
			expected:  "snap-v24-1-n3-s4-0001-01",
		},
		{
			name:      "multi store last store",
			prefix:    "snap",
			version:   "v24.1",
			nodeCount: 3,
			numStores: 4,
			node:      2,
			storeIdx:  4,
			expected:  "snap-v24-1-n3-s4-0002-04",
		},
		{
			name:      "dev version truncated",
			prefix:    "ms-snapshot",
			version:   "v26-3-0-alpha-00000000-dev-11dc408",
			nodeCount: 3,
			numStores: 4,
			node:      1,
			storeIdx:  2,
			expected:  "ms-snapshot-v26-3-0-alpha-00000000-dev-11dc408-n3-s4-0001-02",
		},
	}

	for _, tc := range tests {
		t.Run(tc.name, func(t *testing.T) {
			got := snapshotName(tc.prefix, tc.version, tc.nodeCount, tc.numStores, tc.node, tc.storeIdx)
			assert.Equal(t, tc.expected, got)
		})
	}
}

func TestParseSnapshotName(t *testing.T) {
	tests := []struct {
		name             string
		snapshotName     string
		expectedNodeIdx  int
		expectedStoreIdx int
		expectedErr      string
	}{
		{
			name:             "single store",
			snapshotName:     "snap-v24-1-n3-0001",
			expectedNodeIdx:  1,
			expectedStoreIdx: 1,
		},
		{
			name:             "multi store first",
			snapshotName:     "snap-v24-1-n3-s4-0001-01",
			expectedNodeIdx:  1,
			expectedStoreIdx: 1,
		},
		{
			name:             "multi store last",
			snapshotName:     "snap-v24-1-n3-s4-0003-04",
			expectedNodeIdx:  3,
			expectedStoreIdx: 4,
		},
		{
			name:             "real multi store name",
			snapshotName:     "ms-snapshot-v26-3-0-alpha-00000000-dev-11dc408-n3-s4-0001-02",
			expectedNodeIdx:  1,
			expectedStoreIdx: 2,
		},
		{
			name:             "real single store name",
			snapshotName:     "snap-v24-1-0-n9-0001",
			expectedNodeIdx:  1,
			expectedStoreIdx: 1,
		},
		{
			name:         "too short",
			snapshotName: "x",
			expectedErr:  "unable to parse snapshot name",
		},
		{
			name:         "non-numeric suffix",
			snapshotName: "snap-abc",
			expectedErr:  "unable to parse snapshot name",
		},
	}

	for _, tc := range tests {
		t.Run(tc.name, func(t *testing.T) {
			nodeIdx, storeIdx, err := parseSnapshotName(tc.snapshotName)
			if tc.expectedErr != "" {
				assert.ErrorContains(t, err, tc.expectedErr)
			} else {
				assert.NoError(t, err)
				assert.Equal(t, tc.expectedNodeIdx, nodeIdx)
				assert.Equal(t, tc.expectedStoreIdx, storeIdx)
			}
		})
	}
}

func TestSnapshotNameRoundTrip(t *testing.T) {
	// Verify that names generated by snapshotName can be parsed by
	// parseSnapshotName back to the original node and store indices.
	tests := []struct {
		name      string
		numStores int
		node      int
		storeIdx  int
	}{
		{name: "single store", numStores: 1, node: 1, storeIdx: 1},
		{name: "multi store first", numStores: 4, node: 1, storeIdx: 1},
		{name: "multi store mid", numStores: 4, node: 2, storeIdx: 3},
		{name: "multi store last", numStores: 4, node: 3, storeIdx: 4},
	}

	for _, tc := range tests {
		t.Run(tc.name, func(t *testing.T) {
			name := snapshotName("test", "v24-1", 3, tc.numStores, tc.node, tc.storeIdx)
			nodeIdx, storeIdx, err := parseSnapshotName(name)
			assert.NoError(t, err)
			assert.Equal(t, tc.node, nodeIdx)
			assert.Equal(t, tc.storeIdx, storeIdx)
		})
	}
}

func TestVerifyClusterName(t *testing.T) {
	findActiveAccounts = func(l *logger.Logger) (map[string]string, error) {
		return map[string]string{"1": "user1", "2": "user2", "3": "USER4"}, nil
	}
	defer func() {
		findActiveAccounts = vm.FindActiveAccounts
	}()
	cases := []struct {
		description, clusterName, username string
		errorExpected                      bool
	}{
		{
			"username found", "user1-clustername", "", false,
		},
		{
			"username not found", "user3-clustername", "", true,
		},
		{
			"specified username", "user3-clustername", "user3", false,
		},
		{
			"specified username that doesn't match", "user1-clustername", "fakeuser", true,
		},
		{
			"clustername not sanitized", "UserName-clustername", "", true,
		},
		{
			"no username", "clustername", "", true,
		},
		{
			"no clustername", "user1", "", true,
		},
		{
			"unsanitized found username", "user4-clustername", "", false,
		},
		{
			"unsanitized specified username", "user3-clustername", "USER3", false,
		},
	}
	for _, c := range cases {
		t.Run(c.description, func(t *testing.T) {
			if c.errorExpected {
				assert.Error(t, verifyClusterName(nilLogger(), c.clusterName, c.username))
			} else {
				assert.NoError(t, verifyClusterName(nilLogger(), c.clusterName, c.username))
			}
		})
	}
}
