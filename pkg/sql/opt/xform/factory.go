// listOnlyHasNulls if every item in the given list is a Null op. If the list
// is empty, listOnlyHasNulls returns false.
func (f *factory) listOnlyHasNulls(list opt.ListID) bool {
	if list.Length == 0 {
		return false
	}

	for _, item := range f.mem.lookupList(list) {
		if f.mem.lookupNormExpr(item).op != opt.NullOp {
			return false
		}
	}
	return true
}

// isSortedSet returns true if the list is in sorted order, with no duplicates.
// See the comment for listSorter.compare for comparison rule details.
func (f *factory) isSortedSet(list opt.ListID) bool {
	ls := listSorter{f: f, list: f.mem.lookupList(list)}
	for i := 0; i < int(list.Length-1); i++ {
		if !ls.less(i, i+1) {
			return false
		}
	}
	return true
}

// constructSortedSet sorts the given list and removes duplicates, and returns
// the resulting list. See the comment for listSorter.compare for comparison
// rule details.
func (f *factory) constructSortedSet(list opt.ListID) opt.ListID {
	// Make a copy of the list, since it needs to stay immutable.
	newList := make([]opt.GroupID, list.Length)
	copy(newList, f.mem.lookupList(list))
	ls := listSorter{f: f, list: newList}

	// Sort the list.
	sort.Slice(ls.list, ls.less)

	// Remove duplicates from the list.
	n := 0
	for i := 0; i < int(list.Length-1); i++ {
		if ls.compare(i, i+1) == 0 {
			continue
		}
		newList[n] = newList[i]
		n++
	}
	return f.mem.internList(newList[0 : n+1])
}

// listSorter is a helper struct that implements the sort.Slice "less"
// comparison function.
type listSorter struct {
	f    *factory
	list []opt.GroupID
}

// less returns true if item i in the list compares less than item j.
// sort.Slice uses this method to sort the list.
func (s listSorter) less(i, j int) bool {
	return s.compare(i, j) < 0
}

// compare returns -1 if item i compares less than item j, 0 if they are equal,
// and 1 if item i compares greater. Constants within the list are sorted and
// uniquified according to Datum comparison rules. Non-constants are sorted and
// uniquified by GroupID (arbitrary, but stable).
func (s listSorter) compare(i, j int) int {
	// If both are constant values, then use datum comparison.
	left := s.f.mem.lookupNormExpr(s.list[i])
	if left.isConstValue() {
		right := s.f.mem.lookupNormExpr(s.list[j])
		if right.isConstValue() {
			leftD := extractConstDatum(s.f.mem, makeNormLoc(s.list[i]))
			rightD := extractConstDatum(s.f.mem, makeNormLoc(s.list[j]))
			return leftD.Compare(s.f.evalCtx, rightD)
		}
	}

	// Arbitrarily order by GroupID.
	if s.list[i] < s.list[j] {
		return -1
	} else if s.list[i] > s.list[i] {
		return 1
	}
	return 0
}

