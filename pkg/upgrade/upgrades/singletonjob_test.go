package upgrades

import (
	"testing"

	"github.com/cockroachdb/cockroach/pkg/jobs/jobspb"
	"github.com/cockroachdb/cockroach/pkg/util/leaktest"
	"github.com/cockroachdb/cockroach/pkg/util/log"
	"github.com/stretchr/testify/require"
)

// Verify all static jobs are registered appropriately in the jobs system.
// This test is minimal since we can't depend on starting test server
// due to circular dependencies.  The rest of the functionality of this package
// is tested by jobs/singleton_test.go
func TestSanity(t *testing.T) {
	defer leaktest.AfterTest(t)()
	defer log.Scope(t).Close(t)

	for id, sj := range staticJobs {
		// Static job IDs should be small.  This is an arbitrary
		// restriction which can be relaxed, as long as the static IDs cannot
		// overlap with job IDs generated by job.Registry.MakeJobID
		require.Less(t, id, 1024)

		require.NotEmpty(t, sj.description)

		// Sanity check Details/Progress details.
		require.NotNil(t, sj.details)
		require.NotNil(t, jobspb.WrapPayloadDetails(sj.details))
		require.NotNil(t, sj.progress)
		require.NotNil(t, jobspb.WrapProgressDetails(sj.progress))
	}
}
