package main

import (
	"bufio"
	"flag"
	"fmt"
	"log"
	"os"
	"path/filepath"
	"regexp"
	"sort"
	"strings"
)

var outDir = flag.String("out-dir", "", "path to the root of the cockroach workspace")

func main() {
	flag.Parse()
	if *outDir == "" {
		log.Fatal("You need to pass the -out-dir flag as the workspace root")
	}

	pkgDir := filepath.Join(*outDir, "pkg")
	callSitePattern := regexp.MustCompile(`ash\.SetWorkState\(`)
	commentPattern := regexp.MustCompile(`^\s*//`)

	var callSites []string

	err := filepath.Walk(pkgDir, func(path string, info os.FileInfo, err error) error {
		if err != nil {
			return err
		}
		if info.IsDir() {
			if info.Name() == "vendor" {
				return filepath.SkipDir
			}
			return nil
		}
		if !strings.HasSuffix(path, ".go") {
			return nil
		}
		if strings.HasSuffix(path, "_test.go") {
			return nil
		}

		f, err := os.Open(path)
		if err != nil {
			return err
		}
		defer f.Close()

		scanner := bufio.NewScanner(f)
		lineNum := 0
		for scanner.Scan() {
			lineNum++
			line := scanner.Text()
			if callSitePattern.MatchString(line) && !commentPattern.MatchString(line) {
				relPath, err := filepath.Rel(*outDir, path)
				if err != nil {
					return err
				}
				callSites = append(callSites, fmt.Sprintf("%s:%d", relPath, lineNum))
			}
		}
		return scanner.Err()
	})
	if err != nil {
		log.Fatalf("walking pkg/: %v", err)
	}

	sort.Strings(callSites)

	outputPath := filepath.Join(*outDir, "pkg", "obs", "ash", "ash_integration_points.txt")
	f, err := os.Create(outputPath)
	if err != nil {
		log.Fatalf("creating output file: %v", err)
	}
	defer f.Close()

	w := bufio.NewWriter(f)
	fmt.Fprintln(w, "# ASH integration points")
	fmt.Fprintln(w, "#")
	fmt.Fprintln(w, "# Generated by generate-ash-inventory. DO NOT EDIT.")
	fmt.Fprintln(w, "#")
	fmt.Fprintf(w, "# To regenerate: ./dev generate ash-inventory\n")
	fmt.Fprintln(w, "#")
	fmt.Fprintf(w, "# %d call sites:\n", len(callSites))
	for _, site := range callSites {
		fmt.Fprintln(w, site)
	}
	if err := w.Flush(); err != nil {
		log.Fatalf("writing output: %v", err)
	}

	fmt.Printf("Wrote %d ASH integration points to %s\n", len(callSites), outputPath)
}
