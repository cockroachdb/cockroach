package main

import (
	"bufio"
	"flag"
	"fmt"
	"log"
	"os"
	"path/filepath"
	"regexp"
	"sort"
	"strings"
)

var outDir = flag.String("out-dir", "", "path to the root of the cockroach workspace")

type instrumentationPoint struct {
	pkg       string
	eventType string
	event     string
}

func main() {
	flag.Parse()
	if *outDir == "" {
		log.Fatal("You need to pass the -out-dir flag as the workspace root")
	}

	pkgDir := filepath.Join(*outDir, "pkg")
	callSitePattern := regexp.MustCompile(`ash\.SetWorkState\(`)
	commentPattern := regexp.MustCompile(`^\s*//`)
	// Match the WorkEventType and event arguments from the collected call text.
	// Only matches known WorkEventType constants to avoid matching
	// ash.WorkloadInfo or other ash.Work* identifiers.
	argsPattern := regexp.MustCompile(
		`ash\.(Work(?:CPU|Admission|IO|Network|Lock|Other|Unknown)),\s*(.+?)\s*\)\s*$`,
	)

	var points []instrumentationPoint

	err := filepath.Walk(pkgDir, func(path string, info os.FileInfo, err error) error {
		if err != nil {
			return err
		}
		if info.IsDir() {
			if info.Name() == "vendor" {
				return filepath.SkipDir
			}
			return nil
		}
		if !strings.HasSuffix(path, ".go") {
			return nil
		}
		if strings.HasSuffix(path, "_test.go") {
			return nil
		}

		relPath, err := filepath.Rel(*outDir, path)
		if err != nil {
			return err
		}
		pkgPath := filepath.Dir(relPath)

		f, err := os.Open(path)
		if err != nil {
			return err
		}
		defer f.Close()

		lines, err := readLines(f)
		if err != nil {
			return err
		}

		for i, line := range lines {
			if !callSitePattern.MatchString(line) || commentPattern.MatchString(line) {
				continue
			}
			combined := collectCall(lines, i)
			matches := argsPattern.FindStringSubmatch(combined)
			if matches == nil {
				log.Printf(
					"WARNING: could not parse SetWorkState call at %s:%d",
					relPath, i+1,
				)
				continue
			}
			eventType := matches[1]
			event := strings.Trim(matches[2], `"`)

			points = append(points, instrumentationPoint{
				pkg:       pkgPath,
				eventType: eventType,
				event:     event,
			})
		}
		return nil
	})
	if err != nil {
		log.Fatalf("walking pkg/: %v", err)
	}

	sort.Slice(points, func(i, j int) bool {
		if points[i].event != points[j].event {
			return points[i].event < points[j].event
		}
		if points[i].eventType != points[j].eventType {
			return points[i].eventType < points[j].eventType
		}
		return points[i].pkg < points[j].pkg
	})

	// Remove the old file if it exists.
	oldPath := filepath.Join(
		*outDir, "pkg", "obs", "ash", "ash_integration_points.txt",
	)
	_ = os.Remove(oldPath)

	outputPath := filepath.Join(
		*outDir, "pkg", "obs", "ash", "ash_instrumentation_points.txt",
	)
	outF, err := os.Create(outputPath)
	if err != nil {
		log.Fatalf("creating output file: %v", err)
	}
	defer outF.Close()

	w := bufio.NewWriter(outF)
	fmt.Fprintln(w, "# ASH instrumentation points")
	fmt.Fprintln(w, "#")
	fmt.Fprintln(w, "# Generated by generate-ash-inventory. DO NOT EDIT.")
	fmt.Fprintln(w, "#")
	fmt.Fprintln(w, "# To regenerate: ./dev generate ash-inventory")
	fmt.Fprintln(w, "#")
	fmt.Fprintf(w, "# %d instrumentation points:\n", len(points))

	// Compute column widths for aligned output.
	eventWidth, typeWidth := 0, 0
	for _, p := range points {
		if len(p.event) > eventWidth {
			eventWidth = len(p.event)
		}
		if len(p.eventType) > typeWidth {
			typeWidth = len(p.eventType)
		}
	}
	fmtStr := fmt.Sprintf("%%-%ds  %%-%ds  %%s\n", eventWidth, typeWidth)
	for _, p := range points {
		fmt.Fprintf(w, fmtStr, p.event, p.eventType, p.pkg)
	}
	if err := w.Flush(); err != nil {
		log.Fatalf("writing output: %v", err)
	}

	fmt.Printf("Wrote %d ASH instrumentation points to %s\n", len(points), outputPath)
}

// readLines reads all lines from the given file.
func readLines(f *os.File) ([]string, error) {
	var lines []string
	scanner := bufio.NewScanner(f)
	for scanner.Scan() {
		lines = append(lines, scanner.Text())
	}
	return lines, scanner.Err()
}

// collectCall collects lines starting from startIdx until the parentheses
// opened by ash.SetWorkState( are balanced. The collected lines are joined
// into a single string with spaces.
func collectCall(lines []string, startIdx int) string {
	var parts []string
	depth := 0
	for i := startIdx; i < len(lines); i++ {
		line := strings.TrimSpace(lines[i])
		parts = append(parts, line)
		for _, ch := range line {
			switch ch {
			case '(':
				depth++
			case ')':
				depth--
				if depth == 0 {
					return strings.Join(parts, " ")
				}
			}
		}
	}
	return strings.Join(parts, " ")
}
