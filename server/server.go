// Package server implements a basic HTTP server for interacting with a node.
package server

import (
	"compress/gzip"
	"fmt"
	"io"
	"net/http"
	"strings"
)

type server struct {
	mux *http.ServeMux
}

// New allocates and returns a new server.
func New() *server {
	s := &server{mux: http.NewServeMux()}
	s.init()
	return s
}

func (s *server) init() {
	s.mux.HandleFunc("/healthz", s.handleHealthz)
}

type gzipResponseWriter struct {
	io.Writer
	http.ResponseWriter
}

func (w gzipResponseWriter) Write(b []byte) (int, error) {
	return w.Writer.Write(b)
}

// ServeHTTP is necessary to implement the http.Handler interface. It
// will gzip a response if the appropriate request headers are set.
func (s *server) ServeHTTP(w http.ResponseWriter, r *http.Request) {
	if !strings.Contains(r.Header.Get("Accept-Encoding"), "gzip") {
		s.mux.ServeHTTP(w, r)
		return
	}
	w.Header().Set("Content-Encoding", "gzip")
	gz := gzip.NewWriter(w)
	defer gz.Close()
	s.mux.ServeHTTP(gzipResponseWriter{Writer: gz, ResponseWriter: w}, r)
}

func (s *server) handleHealthz(w http.ResponseWriter, r *http.Request) {
	w.Header().Set("Content-Type", "text/plain")
	fmt.Fprintln(w, "ok")
}
